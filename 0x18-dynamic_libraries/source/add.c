#include <stdio.h>

/**
 *add - adds two values
 *@a: first value
 *@b: second value
 *
 * Return: the value of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - subtract two values
 *@a: first value
*@b: second value
*
* Return: the difference
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
*mul - multiply two values
*@a: first value
*@b: second value
* Return: the value of a and b
*/
int mul(int a, int b)
{
return (a *b);
}

/**
*div - divides two value
*@a: first value
*@b: second value
*
*Return: the value of a and b
*/
int div(int a, int b)
{
	return (a / b);
}

/**
*mod- modulos of two values
*@a: first value
*@b: second value
*
*Return: the value of a and b
*/
int mod(int a, int b)
{
	return (a % b);
}
